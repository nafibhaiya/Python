marks = [91, 93, 90]  # list
print(marks)  # print full list
print(marks[0])
print(marks[0:2])  # will print from 0 to 1, 2nd index will not print
print(marks[-1])  # output from backward
marks.append(99)  # this will add 99 on the list
marks.insert(2, 93.3)  # insert in any index
print(marks)
print(99 in marks)  # will check if 99 exist or not in the list
print(len(marks))  # starts form 1
